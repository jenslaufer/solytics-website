    <h2 class="text-2xl font-bold font-sans-serif mt-10 mb-4">Was sind KI-Agenten?</h2>
    <p class="text-slate-700 leading-relaxed">
      Ein KI-Agent ist ein Softwaresystem, das auf Basis eines großen Sprachmodells (Large Language Model, LLM) eigenständig Aufgaben bearbeitet. Im Gegensatz zu einfachen KI-Tools, die auf eine einzelne Anfrage reagieren, können Agenten mehrstufige Aufgaben planen, Werkzeuge nutzen, Entscheidungen treffen und ihr Vorgehen anpassen.
    </p>
    <p class="text-slate-700 leading-relaxed mt-3">
      Stellen Sie sich einen KI-Agenten wie einen sehr fähigen Mitarbeiter vor, der eine Aufgabe erhält, selbstständig recherchiert, die nötigen Schritte plant, verschiedene Softwaretools nutzt und am Ende ein Ergebnis liefert — ohne dass Sie jeden einzelnen Schritt vorgeben müssen.
    </p>

    <h3 class="text-xl font-bold font-sans-serif mt-6 mb-3">Die Kernfähigkeiten eines KI-Agenten</h3>
    <ul class="list-disc list-inside text-slate-700 mt-3 space-y-2">
      <li><strong>Planung:</strong> Der Agent zerlegt komplexe Aufgaben in Teilschritte und bestimmt die optimale Reihenfolge.</li>
      <li><strong>Tool-Nutzung:</strong> Agenten können APIs aufrufen, Datenbanken abfragen, Dateien lesen und schreiben, E-Mails versenden oder Webseiten analysieren.</li>
      <li><strong>Entscheidungsfähigkeit:</strong> Basierend auf Zwischenergebnissen passt der Agent seine Strategie an — ähnlich wie ein Mensch, der auf unerwartete Situationen reagiert.</li>
      <li><strong>Gedächtnis:</strong> Moderne Agenten können Kontext über mehrere Interaktionen hinweg behalten und aus vergangenen Aufgaben lernen.</li>
    </ul>


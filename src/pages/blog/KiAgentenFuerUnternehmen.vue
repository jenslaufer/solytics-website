    <h2 class="text-2xl font-bold font-sans-serif mt-10 mb-4">KI-Agenten vs. klassische Automatisierung</h2>
    <p class="text-slate-700 leading-relaxed">
      Was unterscheidet KI-Agenten von klassischer Prozessautomatisierung (RPA)? Der entscheidende Unterschied liegt in der Flexibilität. RPA-Bots folgen starren Regeln: Wenn X, dann Y. Sie brechen ab, sobald eine Situation eintritt, die nicht vorhergesehen wurde. KI-Agenten hingegen können mit Unvorhergesehenem umgehen, Kontext verstehen und eigenständig Lösungsstrategien entwickeln.
    </p>
    <p class="text-slate-700 leading-relaxed mt-3">
      Das bedeutet: KI-Agenten eignen sich besonders dort, wo Prozesse variabel sind, natürliche Sprache verarbeitet werden muss oder Entscheidungen auf Basis unstrukturierter Daten getroffen werden. Klassische RPA bleibt sinnvoll für hochstandardisierte, regelbasierte Aufgaben.
    </p>


    <h2>Häufige Fehler bei der ROI-Berechnung</h2>
    <ul>
      <li><strong>Nur direkte Kosten betrachten:</strong> Fehlerkosten, Opportunitätskosten und Skalierungseffekte werden ignoriert. Das unterschätzt den wahren ROI systematisch.</li>
      <li><strong>Stundensatz zu niedrig ansetzen:</strong> Der relevante Stundensatz sind die Brutto-Personalkosten (Gehalt + Sozialabgaben + Arbeitsplatzkosten), nicht das Nettogehalt. Für eine Buchhaltungskraft mit 3.500 € brutto liegt der reale Stundensatz bei 50–60 €.</li>
      <li><strong>Anlaufphase ignorieren:</strong> In den ersten 2–4 Wochen nach Go-live ist die Einsparung geringer, weil Mitarbeiter das System parallel kontrollieren. Planen Sie diese Phase ein.</li>
      <li><strong>Qualitative Vorteile ignorieren:</strong> Schnellere Reaktionszeiten, höhere Kundenzufriedenheit und bessere Datenqualität haben wirtschaftlichen Wert — auch wenn er schwerer zu beziffern ist.</li>
    </ul>

    <h2>Wann sich KI-Automatisierung nicht lohnt</h2>
    <p>
      Ehrlichkeit gehört zur ROI-Berechnung. KI-Automatisierung lohnt sich nicht, wenn:
    </p>
    <ul>
      <li>Der Prozess weniger als 10 Stunden pro Monat bindet — die Einsparung deckt die laufenden Kosten nicht.</li>
      <li>Der Prozess sich ständig grundlegend ändert — die KI muss zu oft neu trainiert oder angepasst werden.</li>
      <li>Keine digitalen Daten vorliegen — die Digitalisierung muss erst erfolgen, bevor KI sinnvoll ist.</li>
      <li>Regulatorische Hürden eine vollständige Automatisierung verhindern — etwa bei Prozessen, die eine persönliche Unterschrift erfordern.</li>
    </ul>


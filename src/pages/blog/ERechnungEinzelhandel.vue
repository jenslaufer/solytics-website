    <h2>Häufige Fragen aus dem Einzelhandel</h2>

    <h3>„Mein Laden verkauft nur an Privatkunden. Betrifft mich die Pflicht?"</h3>
    <p>
      Beim Versand: wahrscheinlich nicht, wenn Sie keine B2B-Kunden haben. Beim Empfang: ja. Ihre Lieferanten werden Ihnen E-Rechnungen schicken. Sie müssen diese verarbeiten können.
    </p>

    <h3>„Mein Großhändler schickt mir plötzlich XML-Dateien statt PDFs. Was tun?"</h3>
    <p>
      Das ist eine XRechnung — und ab 2025 sein gutes Recht. Öffnen Sie die Datei nicht im Browser, sondern importieren Sie sie in Ihre Buchhaltungssoftware. Alternativ gibt es kostenlose Viewer wie den KoSIT XRechnung Viewer.
    </p>

    <h3>„Ich bin Kleinunternehmer. Gilt die Pflicht auch für mich?"</h3>
    <p>
      Ja. Die Kleinunternehmerregelung (§ 19 UStG) befreit nicht von der E-Rechnungspflicht. Empfang seit 2025, Versand spätestens ab 2028. Mehr dazu: <router-link to="/blog/e-rechnung-kleinunternehmer">E-Rechnung für Kleinunternehmer</router-link>.
    </p>


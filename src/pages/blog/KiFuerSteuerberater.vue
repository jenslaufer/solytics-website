    <h2>Datenschutz und Berufsrecht</h2>
    <p>
      Steuerberater unterliegen besonderen Verschwiegenheitspflichten. KI-Lösungen für Kanzleien müssen diese Anforderungen erfüllen:
    </p>
    <ul>
      <li><strong>Datenverarbeitung in der EU:</strong> Alle Mandantendaten werden auf europäischen Servern verarbeitet. Keine Übermittlung in Drittstaaten.</li>
      <li><strong>Auftragsverarbeitungsvertrag (AVV):</strong> Verpflichtend nach DSGVO Art. 28 — mit dem KI-Anbieter abzuschließen.</li>
      <li><strong>Keine Weitergabe an Dritte:</strong> Mandantendaten werden nicht zum Training von Sprachmodellen verwendet.</li>
      <li><strong>GoBD-Konformität:</strong> Alle automatisierten Buchungen sind vollständig nachvollziehbar und revisionssicher dokumentiert.</li>
      <li><strong>Berufsrechtliche Verantwortung:</strong> Die KI unterstützt — die finale Verantwortung bleibt beim Steuerberater. Jede automatische Buchung kann geprüft und korrigiert werden.</li>
    </ul>


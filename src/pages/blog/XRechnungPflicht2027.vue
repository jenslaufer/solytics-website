    <h2 class="text-2xl font-bold font-sans-serif mt-10 mb-4">Was ist die XRechnung?</h2>
    <p class="text-slate-700 leading-relaxed">
      Die XRechnung ist ein XML-basiertes Rechnungsformat, das der europäischen Norm EN 16931 entspricht. Im Gegensatz zu einer PDF-Rechnung enthält eine XRechnung ausschließlich maschinenlesbare, strukturierte Daten. Das bedeutet: Keine manuellen Eingaben mehr, keine OCR-Fehler, keine Medienbrüche. Empfängersysteme können XRechnungen automatisch verarbeiten, validieren und in die Buchhaltung übernehmen.
    </p>
    <p class="text-slate-700 leading-relaxed mt-3">
      Neben der XRechnung erfüllt auch das Format ZUGFeRD (ab Version 2.0) die Anforderungen der EN 16931. ZUGFeRD kombiniert eine PDF-Datei mit eingebetteten XML-Daten und ist damit sowohl menschenlesbar als auch maschinenverarbeitbar. Beide Formate sind für die kommende Pflicht zugelassen.
    </p>


    </p>
    <ul class="list-disc list-inside text-slate-700 mt-3 space-y-2">
      <li><strong>Kapitalgesellschaften</strong> (GmbH, AG, UG) — unabhängig von der Größe</li>
      <li><strong>Personengesellschaften</strong> (OHG, KG, GbR) — sofern sie B2B-Umsätze erzielen</li>
      <li><strong>Einzelunternehmer und Freiberufler</strong> — wenn sie an andere Unternehmen fakturieren</li>
      <li><strong>Vereine und Stiftungen</strong> — soweit sie unternehmerisch tätig sind</li>
    </ul>
    <p class="text-slate-700 leading-relaxed mt-3">
      Ausgenommen sind lediglich Kleinbetragsrechnungen bis 250 Euro und bestimmte steuerfreie Umsätze. B2C-Rechnungen an Privatpersonen sind von der Pflicht nicht betroffen.
    </p>

    <h2 class="text-2xl font-bold font-sans-serif mt-10 mb-4">Warum führt Deutschland die E-Rechnungspflicht ein?</h2>
    <p class="text-slate-700 leading-relaxed">
      Die Einführung ist Teil der EU-Initiative „VAT in the Digital Age" (ViDA). Ziel ist ein europaweites, echtzeitnahes Meldesystem für Umsatzsteuerdaten. Länder wie Italien, Frankreich und Polen haben bereits ähnliche Systeme implementiert oder sind dabei. Deutschland folgt nun mit eigenem Zeitplan.
    </p>
    <p class="text-slate-700 leading-relaxed mt-3">
      Für die Finanzverwaltung bedeutet das: weniger Umsatzsteuerbetrug, schnellere Prüfungen und ein lückenloses digitales Bild aller B2B-Transaktionen. Für Unternehmen bedeutet es: die Digitalisierung der Rechnungsstellung ist nicht mehr optional, sondern gesetzlich vorgeschrieben.
    </p>


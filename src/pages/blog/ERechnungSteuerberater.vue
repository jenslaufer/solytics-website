    <h2>GoBD-Compliance: Archivierung richtig umsetzen</h2>
    <p>
      E-Rechnungen unterliegen den Grundsätzen ordnungsmäßiger Buchführung und Dokumentation (GoBD). Das Thema Archivierung ist ein häufiger Stolperstein — und damit ein wichtiges Beratungsfeld:
    </p>
    <ul>
      <li><strong>Unveränderbarkeit:</strong> E-Rechnungen müssen im Originalformat archiviert werden. Eine XRechnung als XML, eine ZUGFeRD-Rechnung als PDF/A-3. Ausdrucken und abheften reicht nicht.</li>
      <li><strong>Aufbewahrungsfrist:</strong> 10 Jahre, gerechnet ab Ende des Kalenderjahres der Rechnungsstellung.</li>
      <li><strong>Verfahrensdokumentation:</strong> Der Mandant muss dokumentieren, wie E-Rechnungen empfangen, verarbeitet und archiviert werden. Ohne Verfahrensdokumentation droht Ärger bei der Betriebsprüfung.</li>
      <li><strong>Revisionssicheres Archiv:</strong> Ein einfacher Dateiordner genügt nicht. Mandanten brauchen ein DMS oder eine Archivierungslösung, die Zugriffskontrolle, Versionierung und Protokollierung bietet.</li>
      <li><strong>Konvertierungsverbot:</strong> Das Original-XML darf nicht in ein anderes Format konvertiert und dann nur in der konvertierten Form archiviert werden.</li>
    </ul>
    <p>
      Steuerberater sollten die Verfahrensdokumentation als festen Bestandteil der E-Rechnungsberatung anbieten. Viele Mandanten unterschätzen diesen Punkt — bis zur nächsten Betriebsprüfung.
    </p>

